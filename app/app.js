function initListeners() {
  $("nav a").click(function (e) {
    let btnID = e.currentTarget.id;
    loadPageContent(btnID);
    console.log("click " + contentID);
  });
}

function loadPageContent(pageName) {
  let contentID = pageName + "Content";
  $("#app").html(eval(contentID));
}

function initApp() {
  initListeners();
}

$(document).ready(function () {
  loadPageContent("home");
  //   comment the loadPageContent out to keep the content on the page and style it before pushing it over to the app.js file. you can cheat the loading process by changing the loadPageContent to "about" will load that automatically
  initApp();
});
